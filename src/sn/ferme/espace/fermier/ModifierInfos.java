    public void modifierInfos() throws SQLException {
        String nom = txtNom.getText();
        String prenom = txtPrenom.getText();
        String email = txtEmail.getText();
        String telephone = txtTelephone.getText();
        String adresse = txtAdresse.getText();

        if (nom.isEmpty() || prenom.isEmpty() || email.isEmpty() || telephone.isEmpty() || adresse.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Veuillez renseignez tous les champs");
        } else if (!vc.validerMail(email)) {
            JOptionPane.showMessageDialog(null, "Format adresse email invalide");
        } else {
            userService.updateUser(user.getIdUtilisateur(), nom, prenom, email, telephone, adresse);
        }
    }

